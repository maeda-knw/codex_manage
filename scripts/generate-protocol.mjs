import { readFile, readdir, rename, rm, writeFile } from 'node:fs/promises';
import { spawnSync } from 'node:child_process';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repositoryRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const outputDirectory = join(repositoryRoot, 'src', 'codex', 'protocol', 'generated');
const temporaryDirectory = `${outputDirectory}.tmp-${process.pid}`;
const codexPath = process.execPath;
const codexPrefixArgs = [join(repositoryRoot, 'node_modules', '@openai', 'codex', 'bin', 'codex.js')];

await rm(temporaryDirectory, { recursive: true, force: true });

try {
  const generation = spawnSync(codexPath, [...codexPrefixArgs, 'app-server', 'generate-ts', '--out', temporaryDirectory], {
    cwd: repositoryRoot,
    encoding: 'utf8',
    stdio: ['ignore', 'inherit', 'inherit'],
    windowsHide: true
  });

  if (generation.error) {
    throw generation.error;
  }

  if (generation.status !== 0) {
    throw new Error(`codex app-server generate-ts exited with status ${generation.status ?? 'unknown'}.`);
  }

  const versionResult = spawnSync(codexPath, [...codexPrefixArgs, '--version'], {
    cwd: repositoryRoot,
    encoding: 'utf8',
    windowsHide: true
  });
  if (versionResult.error) {
    throw versionResult.error;
  }
  if (versionResult.status !== 0) {
    throw new Error(`codex --version exited with status ${versionResult.status ?? 'unknown'}.`);
  }
  const versionOutput = versionResult.stdout.trim();
  const version = /(?:codex-cli\s+)?(\d+\.\d+\.\d+(?:[-+][0-9A-Za-z.-]+)?)/u.exec(versionOutput)?.[1];
  if (!version) {
    throw new Error(`Could not parse the pinned Codex CLI version from: ${versionOutput}`);
  }

  await writeFile(
    join(temporaryDirectory, 'README.md'),
    [
      '# Generated Codex App Server protocol',
      '',
      `Generated with \`codex-cli ${version}\`.`,
      '',
      'Do not edit these files by hand. Run `npm run generate:protocol` to replace the snapshot.',
      ''
    ].join('\n'),
    'utf8'
  );
  await writeFile(
    join(temporaryDirectory, 'version.ts'),
    [
      '// Generated by scripts/generate-protocol.mjs alongside the protocol snapshot.',
      `export const GENERATED_CODEX_CLI_VERSION = '${version}' as const;`,
      ''
    ].join('\n'),
    'utf8'
  );
  if (process.platform === 'win32') {
    await normalizeGeneratedLineEndings(temporaryDirectory);
  }

  await rm(outputDirectory, { recursive: true, force: true });
  await rename(temporaryDirectory, outputDirectory);
  console.log(`Generated Codex App Server protocol with codex-cli ${version}.`);
} catch (error) {
  await rm(temporaryDirectory, { recursive: true, force: true });
  throw error;
}

async function normalizeGeneratedLineEndings(directory) {
  for (const entry of await readdir(directory, { withFileTypes: true })) {
    const path = join(directory, entry.name);
    if (entry.isDirectory()) {
      await normalizeGeneratedLineEndings(path);
    } else if (entry.isFile() && (entry.name.endsWith('.ts') || entry.name.endsWith('.md'))) {
      const contents = await readFile(path, 'utf8');
      await writeFile(path, contents.replace(/\r?\n/gu, '\r\n'), 'utf8');
    }
  }
}
